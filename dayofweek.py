num = int(input("Enter a number to check Day of Week 1 to 7:"))

if num == 1:
    print("Monday")
    
elif num == 2:
    print("Tuesday")


elif num == 3:
    print("Wednesday")

elif num == 4:
    print("Thursday")

elif num == 5:
    print("Friday")

elif num == 6:
    print("Saturday")

elif num == 1:
    print("Sunday")

else:
    print("Enter a valid Number from 1 to 7")

